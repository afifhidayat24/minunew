      <h5>
        SEJARAH BERDIRINYA
        MI NU 2 KRADENAN


        MI NU 2 KRADENAN didirikan sekitar tahun 1949 oleh para Pinisepuh/ Pendahulu dan para tokoh masyarakat.
        Dengan mengadakan sekolah darurat di gedung bekas koperasi masjid Al Azhar + ada 20 siswa.
        Semakin hari semakin bertambah banyak siswanya sehingga ruang yang digunakan tidakmencukupi,
        kemudian proses belajar mengajar dipindahkan di mushola Bapak Abdul Majid yang dijadikan 2 ( dua ) kelas.
        Karena kebutuhan gedung yang semakin mendesak pada tahun 1953 dimulailah pembangunan madrasah pada sebidang tanah wakaf
        dari Bapak H. Abdul Ghoni dengan motor penggerak para pinisepuh antara lain : Bapak Ahmad Konari, Bapak Muslim,
        Bapak H. Abdul Ghoni, Bapak Rusdi dan lain-lain.

        Pada tanggal 1 April 1953 gedung tersebut resmi ditempati, walaupun hanya 4 (empat) kelas,
        pada waktu itu diangkat pula Kepala Sekolah yang pertama yaitu Bapak Imam Subari.
        Karena pertambahan murid yang sangat pesat maka sekitar tahun 1970 bangunan ditambah 2 (dua) kelas
        di atas tanah wakaf dari Bapak Ahmad Zaini.Pembangunan ini diprakasai oleh Bapak Ahmad Sujud dengan
        kepala madrasah Bapak H. Nur Al Fatah sehingga madrasah mempunyai 6 (enam) Kelas.
        Sejak pertama kali didirikan madrasah ini bernama MI ISLAMIYAH kemudian pada tahun pada tahun 1979
        ada peraturan dari departemen agama bahwa semua Madrasah yang belum berbadan hukum diharap
        untuk menjadi Madrasah Ibtidaiyah yang dibawah naungan yayasan yang sudah berbadan hukum.
        Karena semua pendiri dan lingkungannya orang Nahdlatul Ulama’, maka MI ISLAMIYAH bernaung dibawah
        yayasan NU yaitu Lembaga Pendidikan Ma’arif.Karena di desa Kradenan ada 3 (tiga) lembaga pendidikan
        maka berdasarkan urutan wilayah maka MI ISLAMIYAH berubah menjadi MI NU KRADENAN III.
        MI NU KRADENAN III juga berada dibawah naungan Departemen Agama dan telah mendapat Piagam terdaftar
        pada 1 April 1991 dan piagam Di Akui pada 1 desember 1998.Pada tanggal 14 Mei 2003
        nama MINU Kradenan III berubah dengan nama MINU Kradenan II sesuai dengan ketentuan
        Dinas Pendidikan Kabupaten Banyuwangi dengan Nomor Identitas Sekolah ( NIS ) 110120.
        Demikian sejarah singkat berdirinya MI NU KRADENAN III.

        Kradenan, 10 Maret 2005
